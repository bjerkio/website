  <h1>Alminnelige betingelser for Bjerk AS</h1>
  <p>Her kan du lese om Bjerks avtalevilkår</p>
  <h2>Betingelser</h2>
  <p>
    Disse generelle avtalevilkår gjelder ved leveranse av tjenester og produkter (Leveransen) fra
    Bjerk AS (Leverandøren), såfremt ikke annet er skriftlig avtalt mellom Leverandøren og kunden
    (Kunden) og/eller regulert i særskilte betingelser knyttet til den enkelte del av Leveransen.
  </p>
  <h2>Definisjon</h2>
  <p>
    Med Bjerk AS menes Bjerk AS med organisasjonsnummer 998 732 867 registrert
    Brønnøysundregistrene. Med «Kunden» menes den person eller instans som bestiller en tjeneste
    eller et produkt fra Leverandøren.
  </p>
  <h2>Elektronisk post</h2>
  <p>
    Kunden godtar at opplysninger og dokumenter, herunder alle fakturaer, purringer og
    inkassovarsel, i sakens anledning sendes og lagres elektronisk - herunder som e-post.
  </p>
  <h2>Underleverandører</h2>
  <p>
    Leverandøren har rett til å benytte underleverandører/tredjeparter i forbindelse med Leveransen.
  </p>
  <h2>Korrekt informasjon</h2>
  <p>
    Kunden forplikter seg til å gi Leverandøren all relevant informasjon i sakens anledning, samt og
    å holde slik informasjon oppdatert. Dette gjelder også Kundens egen kontaktinformasjon.
    Likeledes skal Kunden gi Leverandøren varsel om alle aktuelle henvendelser fra tredjemann
    direkte til Kunden.<br /><br /> Dersom Leverandøren, som følge av Kundes brudd på pliktene i dette
    punkt, påføres ansvar overfor tredjemann eller andre former for kostnader, bærer Kunden ansvaret
    for disse.
  </p>
  <h2>Priser / Merverdiavgift</h2>
  <p>
    Leverandørens veiledende priser fremgår på nettsiden, prisliste eller ved spesifisert
    tilbud/Avtale. Nettsiden oppdateres regelmessig. Prisen som angis her er ikke å anse som bindene
    for Leverandøren, all tid disse når som helst kan justeres.
    <br /><br />
    Pris oppgitt i Avtale vil gjelde for den Leveranse som er omfattet av Avtalen. Dersom pris ikke er
    oppgitt i Avtalen, vil pris oppgitt i Leverandørens tilbud gjelde. Dersom det ikke er utstedt tilbud,
    gjelder prisen angitt på faktura gjeldende de tjenester og produkter som er omfattet av fakturaen.
    <br /><br />
    Alle priser er oppgitt eks. avgifter med mindre annet er særskilt angitt. Ulike faktorer avgjør hvilken
    MVA-sats som belastes, blant annet hvor Kunden er lokalisert. Beregning av MVA vil spesifiseres på
    faktura, og satser fremgår av egen oversikt.
    <br /><br />
    Avtalte priser kan endres ved hvert årsskifte tilsvarende økningen i Statistisk sentralbyrås konsumprisindeks
    (hovedindeksen), første gang med utgangspunkt i indeksen for den måned Avtale ble inngått. Avtalte
    priser kan også endres i den utstrekning regler eller vedtak for offentlige avgifter endres med virkning
    for Leverandørens vederlag eller kostnader.
    <br /><br />
    Endringer i priser for tredjepartsleveranser som inngår i Leveransen gir krav på prisendringer for
    Leveransen med mindre annet er særskilt avtalt.
  </p>
  <h2>Fakturering og betalingsbetingelser</h2>
  <p>
    Vederlag forfaller etter faktura per 10 (ti) kalenderdager.
    <br /><br />
    Dersom vederlaget ikke betales eller holdes tilbake av betalingsformidler eller av andre grunner
    ikke innbetales korrekt til Leverandøren, er bestillingen likefullt bindende for Kunde. Korrekt betaling
    skal i disse tilfeller skje umiddelbart.
    <br /><br />
    Leverandøren og Leverandørens samarbeidspartnere kan fakturere ekstra for eventuelle møter med Kunden,
    og for ekstraordinært arbeid som Kunden ønsker utført og som ikke er inkludert i Avtalen. Likeledes
    kan det faktureres ekstra for ekstraordinære omkostninger. Eventuell ekstra fakturering skal opplyses
    Kunden på forhånd.
    <br /><br />
    Ved forsinket betaling beregnes forsinkelsesrente i henhold til lov 17. desember 1976 nr. 100 om
    renter ved forsinket betaling m.m. (forsinkelsesrenteloven). Det kan også pålegges et purregebyr
    i henhold til de enhver tid gjeldende reguleringer
  </p>
  <h2>Betalingsmislighold</h2>
  <p>
    Dersom forfalt uomtvistet vederlag med tillegg av forsinkelsesrenter ikke er betalt innen 30
    (tretti) kalenderdager fra forfall, kan Leverandøren sende skriftlig varsel til Kunden om at
    Avtalen vil bli hevet dersom oppgjør ikke er skjedd innen 60 (seksti) kalenderdager etter at
    varselet er mottatt.
  </p>
  <h2>Kredittsjekk</h2>
  <p>
    Alle kunder kan bli kredittsjekket. Leverandøren forbeholder seg retten til å gi avslag på
    kreditt uten nærmere begrunnelse.
  </p>

  <h2>Avbestilling</h2>
  <p>
    Kunden kan avbestille Leveransen, helt eller delvis, i den grad det er avtalt. Kunden skal i så
    tilfelle betale for den del av Leveransen som er levert, samt Leverandørens kostnader og tap
    knyttet til avbestillingen.
  </p>
  <h2>Tredjemann</h2>
  <p>
    Verken Leverandøren, Leverandørens ansatte eller Leverandørens samarbeidspartnere kan gi
    bindende forsikringer om at Kunden sine kontoer, profiler, nettsider og lignende vil og/eller
    kan a) bli opprettet eller opplastet til tredjemanns systemer (for eksempel Facebook); b) bli
    godkjent eller publisert av tredjemann (for eksempel Facebook); c) ikke bli fjernet og/eller
    slettet av tredjemann (for eksempel Facebook) etter opprettelse og publisering.
    <br /><br />
    En Leveranse kan ikke kanselleres eller refunderes, selv om et eller flere av produktene eller tjenestene
    levert av Leverandøren ikke blir godkjent opprettet, opplastet, publisert, fjernet/slettet eller
    lignende av tredjemann (for eksempel Facebook) og dette ikke skyldes forhold på Leverandørens side.
    Under enhver omstendighet kan ikke Leverandøren, Leverandørens ansatte eller Leverandørens samarbeidspartnere
    holdes ansvarlig dersom innhold, informasjon og lignende fra Kunden strider mot tredjemanns rettigheter.
  </p>
  <h2>Reklamasjon</h2>
  <p>
    Reklamasjoner må fremmes innen forfall på mottatt faktura gjeldende den aktuelle tjeneste og
    produkt det reklameres på. Reklamasjoner etter dette tidspunkt skal anses som for sent fremsatt
    og bortfaller.
  </p>
  <h2>Fullmakt</h2>
  <p>
    Kunden gir Leverandøren ubegrenset fullmakt til, til enhver tid, å aksessere alle profiler,
    kontoer, sider og lignende som er nødvendig for det arbeid Leverandøren skal utføre, utfører,
    eller har utført. Herunder kan Leverandøren, til enhver tid, ha registrert sin egen, eller
    samarbeidspartnere, sine administrasjonsaksesser på Kunden sine profiler, kontoer, sider og
    lignende. Kunden gir Leverandøren ubegrenset fullmakt til å opprette, oppdatere, endre og slette
    alle profiler, kontoer, sider og lignende som er bestilt av kunden.
  </p>
  <h2>Salgspant/Forbehold om eiendomsrett</h2>
  <p>
    Leverandøren beholder eiendomsretten til Leveransen, inntil vederlaget i sin helhet er betalt,
    med tillegg av evt. renter og omkostninger, ihht. lov om pant.
  </p>
  <h2>Tolkning av de alminnelige betingelsene</h2>
  <p>
    Hvis en del av disse generelle avtalevilkårene blir kjent ugyldig eller ugjennomførlig i henhold
    til gjeldende lovgivning, skal den ugyldige eller ugjennomførlige bestemmelsen anses for å være
    erstattet av en gyldig, gjennomførlig bestemmelse som i størst mulig grad tilsvarer hensikten
    med den opprinnelige bestemmelsen, og resten av avtalevilkårene vil fortsette å gjelde.
    <br /><br />
    Punktoverskriftene i disse generelle avtalevilkårene brukes utelukkende av bekvemmelighetshensyn
    og har ingen rettslig eller kontraktsmessig betydning.
  </p>
  <h2>Bruk i referansesammenheng</h2>
  <p>
    Leverandøren har rett til å bruke alt som innbefattes i Leveransen i referansesammenheng. Dette
    innbefatter blant annet, men ikke begrenset til, å opplyse om at Leverandøren har et
    kundeforhold med Kunden, bruk av Kunden sin logo, presentasjon av arbeidet som er lagt ned og
    lignende.
  </p>
  <h2>Overdragelse av rettigheter og plikter</h2>
  <p>
    Leverandøren kan overdra Avtalen/Leveransen, helt eller delvis, når som helst, med eller uten
    varsel til Kunden. Kunden har ikke rett til å overdra Avtalen/Leveransen uten etter skriftlig
    samtykke fra Leverandøren.
  </p>
  <h2>Tap av data</h2>
  <p>Leverandøren er ikke i noe tilfelle ansvarlig for tap av data/lagret informasjon.</p>
  <h2>Ansvar og erstatningsplikt</h2>
  <p>
    Kunden plikter å holde Leverandøren, Leverandørens ansatte og Leverandørens samarbeidspartnere
    skadesløse, også overfor tredjemann, for enhver skade og et hvert tap i forbindelse med at
    Leveransen ikke kan leveres som avtalt.
    <br /><br />
    Leverandøren har intet ansvar for økonomisk tap som påføres Kunden, så som, men ikke begrenset til,
    uteblitt gevinst, forventet besparelse, tap av inntekt/fortjeneste, tap av omdømme, krav fra tredjeparter,
    herunder krav fra tredjepartsleverandør som følge av Kundens brudd på tredjepartsvilkår eller tap
    som følge av endringer i lov eller som følge av avgjørelser truffet av tredjeparter, forvaltningsorganer
    eller domstol, tvangsmulkt, skade på annen eiendom forårsaket av tjenestens / produktet og/eller
    tjenestens/produktets egenskaper eller andre indirekte skader, selv om Leverandøren blir gjort oppmerksom
    på muligheter av at sådan skade kan oppstå.
    <br /><br />
    Kundens eventuelle krav på vederlagsjustering og Leverandøren sitt ansvar for skade er i alle tilfelle
    begrenset til sammenlagt 50 % av vederlaget som Kunden er fakturert de siste 12 månedene før reklamasjonsdato,
    så fremt Leverandøren ikke har gjort seg skyldig i grov uaktsomhet eller forsett.
  </p>
  <h2>Næringskjøp</h2>
  <p>
    Kunden er kjent med at alle tjenester levert av Leverandøren er å anse som næringskjøp og at
    særlige bestemmelser for forbrukerkjøp således ikke kommer til anvendelse.
  </p>
  <h2>Taushetsplikt</h2>
  <p>
    Taushetsbelagt informasjon som partene blir kjent med i forbindelse med Leveransen og
    gjennomføringen av Leveransen, skal behandles konfidensielt og ikke gjøres tilgjengelig for
    utenforstående uten samtykke fra den annen part.
    <br /><br />
    Taushetsplikt etter denne bestemmelsen er ikke til hinder for utlevering av informasjon som kreves
    fremlagt i henhold til lov eller forskrift. Om mulig skal den annen part varsles før slik informasjon
    gis. Taushetsplikten er ikke til hinder for at opplysningene brukes når ingen berettiget interesse
    tilsier at de holdes hemmelig, for eksempel når de er allment kjent eller er alminnelig tilgjengelig
    andre steder. Partene skal ta nødvendige forholdsregler for å sikre at uvedkommende ikke får innsyn
    i eller kan bli kjent med taushetsbelagt informasjon.
    <br /><br />
    Taushetsplikten gjelder partenes ansatte, underleverandører og tredjeparter som handler på partenes
    vegne i forbindelse med gjennomføring av Avtalen.
    <br /><br />
    Taushetsplikten er ikke til hinder for at partene kan utnytte erfaring og kompetanse som opparbeides
    i forbindelse med gjennomføringen av Avtalen.
    <br /><br />
    Taushetsplikten gjelder også etter at Avtalen er opphørt. Ansatte eller andre som fratrer sin tjeneste
    hos en av partene, skal pålegges taushetsplikt også etter fratredelsen om forhold som nevnt ovenfor.
    Taushetsplikten opphører 5 (fem) år etter Avtalens opphør, med mindre annet følger av lov eller forskrift.
  </p>
  <h2>Force Majeure</h2>
  <p>
    Skulle det inntreffe en ekstraordinær situasjon, som gjør det umulig å oppfylle plikter etter
    Avtalen, og som etter norsk rett må regnes som force majeure, så som, men ikke begrenset til
    streik, lockout, krig eller andre omstendigheter utenfor enn parts kontroll, skal motparten
    varsles om dette så raskt som mulig. Den rammede parts forpliktelser suspenderes så lenge den
    ekstraordinære situasjonen varer. Den annen parts motytelse suspenderes i samme tidsrom.
    <br /><br />
    Motparten kan i force majeure-situasjoner bare avslutte Avtalen med den rammede parts samtykke, eller
    hvis situasjonen varer eller antas å ville vare lenger enn 60 (seksti) kalenderdager, regnet fra
    det tidspunkt situasjonen inntrer, og da bare med 14 (fjorten) kalenderdagers varsel. Hver av partene
    dekker egne kostnader knyttet til avslutning av avtaleforholdet. Kunden betaler avtalt pris for den
    del av Leveransen som var kontraktsmessig levert før Avtalen ble avsluttet. Partene kan ikke rette
    andre krav mot hverandre som følge av avslutning av Avtalen etter denne bestemmelsen.
    <br /><br />
    I forbindelse med force majeure-situasjoner har partene gjensidig informasjonsplikt overfor hverandre
    om alle forhold som må antas å være av betydning for den annen part. Slik informasjon skal gis så
    raskt som mulig.
  </p>
  <h2>Lovvalg og jurisdiksjon</h2>
  <p>
    Enhver konflikt mellom Kunden og Leverandøren i forbindelse med Leveransen og som er omfattet av
    nærværende avtalevilkår reguleres av norsk rett.
    <br /><br />
    Partene vedtar Oslo tingrett, Norge, som verneting. Dette valg av verneting skal imidlertid ikke
    hindre Leverandøren fra å forfølge sine eier- og immaterielle rettigheter (herunder men ikke begrenset
    til å søke midlertidig forføyning) i andre jurisdiksjoner og i henhold til de prosedyrer og regler
    som gjelder på slikt sted.
  </p>
